#include "libft.h"

void	ft_putnbr(int n)
{
	long num;

	num = n;
	if (num == -2147483648)
		ft_putstr("-2147483648");
	if (num < 0)
	{
		ft_putchar('-');
		num = -n;
	}
	if (num > 9)
		ft_putnbr(num / 10);
	ft_putchar((num % 10) + '0');
}

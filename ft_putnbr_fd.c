#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long num;

	num = n;
	if (num == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	if (num < 0)
	{
		ft_putchar_fd('-', fd);
		num = -n;
	}
	if (num > 9)
		ft_putnbr_fd(num / 10, fd);
	ft_putchar_fd((num % 10) + '0', fd);
}
